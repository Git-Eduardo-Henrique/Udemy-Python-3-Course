# float() = transforma numero de string em flutuante
# string() = transforma numero em texto string
# bool() = transforma algum valor em false ou true (1)(0)

print("'1' convertido para numero inteiro:", int("1"), type(int("1")))
print("'2.2' convertido para numero flutuante:", float("2.2"), type(float("2.2")))
print("3 convertido para string:", str(3), type(str(3)))
print("1 convertido para boleano:", bool(1), type(bool(1)))

